    private void tbldanhsachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldanhsachMouseClicked
       int row = tbldanhsach.getSelectedRow();
       if(row == -1){
           return;
       }
        txtMaNhanVien.setText(tbldanhsach.getValueAt(row, 0).toString());
        txtMatkhau.setText(tbldanhsach.getValueAt(row, 1).toString());
        txtXacNhanMatKhau.setText(tbldanhsach.getValueAt(row, 1).toString());
        txtHoTen.setText(tbldanhsach.getValueAt(row, 2).toString());
       if(tbldanhsach.getValueAt(row, 3).toString().equalsIgnoreCase("nhanvien")){
           rdoNhanVien.setSelected(true);
       }else{
           rdoTruongPhong.setSelected(true);
       }
    }//GEN-LAST:event_tbldanhsachMouseClicked

