    private void dtm() {
        DefaultTableModel dtm  = (DefaultTableModel) this.tbldanhsach.getModel();
        dtm.setRowCount(0);
        
        for (nhanvien object :sernhanvien.getallnhanvien()) {
            Object [] obj ={
                object.getManv(),
                object.getMatkhau(),
                object.getTen(),
                object.getVaitro(),
            };
            dtm.addRow(obj);
        }
        
    }
   public nhanvien laydulieu(){ 
       int vaitro = 1;
       nhanvien nv = new nhanvien();
       nv.setManv(txtMaNhanVien.getText());
       nv.setMatkhau(txtMatkhau.getText());
       nv.setTen(txtHoTen.getText());
       if(rdoNhanVien.isSelected() == true){
           vaitro = 0;
       }
       nv.setVaitro(String.valueOf(vaitro));
       return nv;
   }
   
