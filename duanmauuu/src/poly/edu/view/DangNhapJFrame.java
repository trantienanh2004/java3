    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
       List<nhanvien> dn = sernhanvien.getallnhanvien();
      if(txtTenTaiKhoan.getText().trim().length()<=0||txtMatKhau.getText().trim().length()<=0){
           JOptionPane.showMessageDialog(this, "đừng để trống ");
          }else{  
        for (int i = 0; i < dn.size(); i++) {
            if (txtTenTaiKhoan.getText().equalsIgnoreCase(dn.get(i).getManv())&&txtMatKhau.getText().equalsIgnoreCase(dn.get(i).getMatkhau())) {
                 JOptionPane.showMessageDialog(this,"thành công");
                break;
            }else{
                JOptionPane.showMessageDialog(this,"thất bại");
                break;
            }
            
        }
        System.out.println(""+txtTenTaiKhoan.getText().trim().length());
       }
    
    }//GEN-LAST:event_btnDangNhapActionPerformed

