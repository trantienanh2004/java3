    public jsliderJFrame() {
        initComponents();
        setLocationRelativeTo(null);
        Hashtable data = new Hashtable();
        String[] months = {"Jan", "Fed", "Mar", "Apr", "May", "Jun",
            "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"};
        for (int i = 0; i < 12; i++) {
            data.put(i, new JLabel(months[i]));
        }
        sdlmonth.setLabelTable(data);
        Calendar can = Calendar.getInstance();
        sldDay.setValue(can.get(Calendar.DAY_OF_MONTH));
        sdlmonth.setValue(can.get(Calendar.MONTH));
        sdlyear.setValue(can.get(Calendar.YEAR));
        displaydata();
    }
    public void displaydata(){
        SimpleDateFormat sdf = new SimpleDateFormat("EEE,dd-MMM-YYYY");
        int dd = sldDay.getValue();
        int mm = sdlmonth.getValue() ;
        int yy = sdlyear.getValue()-1900;
        Date d = new Date(yy, mm, dd);
        lbi.setText(sdf.format(d));
    }

