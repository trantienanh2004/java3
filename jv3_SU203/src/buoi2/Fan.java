package buoi2;



/**
 *
 * @author HP
 */
public class Fan {
    String ten;
    String hang;
    Double gia;
    String trangthai;

    public Fan(String ten, String hang, Double gia, String trangthai) {
        this.ten = ten;
        this.hang = hang;
        this.gia = gia;
        this.trangthai = trangthai;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getHang() {
        return hang;
    }

    public void setHang(String hang) {
        this.hang = hang;
    }

    public Double getGia() {
        return gia;
    }

    public void setGia(Double gia) {
        this.gia = gia;
    }

    public String getTrangthai() {
        return trangthai;
    }

    public void setTrangthai(String trangthai) {
        this.trangthai = trangthai;
    }

    

   
    
}
